// Estrutura Condicional (Condição)
// IF = Se
// ELSE = Senão
// ELSE IF = Senão se
// SE a primeira porta estiver aberta, você entra, SE NÃO, vai pela segunda.


let num1 = 2;
let num2 = 2;

if (num1 > num2) { 
  console.log('num1 é maior');
} else if (num1 < num2) {
  console.log('num1 é menor');
} else {
  console.log('num1 é igual');
}
